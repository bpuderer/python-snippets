if not 0:
    print("zero value int is False")

if not 0.0:
    print("zero value float is False")

if not None:
    print("None is False")

if not '':
    print("empty string is False")

if not []:
    print("empty list is False")

if not ():
    print("empty tuple is False")

if not {}:
    print("empty dict is False")

if not set():
    print("empty set is False")
